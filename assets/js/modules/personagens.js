class Personagems {
    nome 
    power 
    ataque  
    defesa  
    habiliDescri 
    tripulacao 
    classe
    personaBack

    constructor(nome, power, ataque, defesa, habiliDescri, tripulacao, classe, personaBack) {
        this.nome = nome
        this.power = power;
        this.ataque = ataque;
        this.defesa = defesa;
        this.habiliDescri = habiliDescri;
        this.tripulacao = tripulacao;
        this.classe = classe
        this.personaBack = personaBack
    }
}

export { Personagems };