class Personagems {
    nome 
    power 
    ataque  
    defesa  
    habiliDescri 
    tripulacao 
    classe
    foto
    corMoldura

    constructor(nome, power, ataque, defesa, habiliDescri, tripulacao, classe, foto, corMoldura) {
        this.nome = nome
        this.power = power;
        this.ataque = ataque;
        this.defesa = defesa;
        this.habiliDescri = habiliDescri;
        this.tripulacao = tripulacao;
        this.classe = classe
        this.foto = foto
        this.corMoldura = corMoldura
    }
}

export { Personagems };